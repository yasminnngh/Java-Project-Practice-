package Practice;

public class OddEven {

	public static void main (String [] args) {
		
	System.out.println("Odd");
	for (int i = 0; i <= 100; i++) {
		
		if (i % 2 == 0) {
			
		continue; 
		
	}
	
		System.out.print(i + " ");
		
		}
		
		System.out.println("Even");
		for (int i = 0; i <= 100; i++) {
			
			if (i % 2 != 0) {
				continue; 
			}
		
			System.out.print( i + " ");
			
		}
	
	System.out.println();
	
	
	
	
	
	
	
	
	
	
	
	
	
	
	
	
	
	
	
	
	
	
	}
	
}
